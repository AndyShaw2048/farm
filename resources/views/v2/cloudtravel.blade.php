    $(window).bind("scroll", function () {
        var top = $(this).scrollTop(); // 当前窗口的滚动距离
        // console.log($(this))
        if (top > 1) {
            $(".nav").css({
                "background": "white",
                "opacity": "1",
                "transition": "background-color 1s"
            });
            $(".nav-table a").css("color", "black");
            $(".company a").css("color", "black");
            $(".logo span").css("color", "black");
            $(".am-input-group").css("opacity", "1")
            $(".title").attr("src", "img/标题-黑.png")
        }
        if (top == 0) {
            $(".nav").css({
                "background": "none",
                "opacity": "1",
            });
            $(".nav-table a").css("color", "white");
            $(".company a").css("color", "white");
            $(".logo span").css("color", "white");
            $(".am-input-group").css("opacity", "0.5")
            $(".title").attr("src", "img/标题.png")
        }
    });
    // <!--地图 -->
    function drawMap() {
        require.config({
            paths: {
                echarts: 'assets/echarts-2.2.7/build/dist'
            }
        });
        require(
                [
                    'echarts',
                    'echarts/chart/map' // 使用柱状图就加载bar模块，按需加载
                ],
                function (ec) {

                    // --- 地图 ---
                    var myChart = ec.init(document.getElementById('main'));
                    myChart.setOption({
                        tooltip: {
                            trigger: 'item',
                            formatter: '{b}'
                        },
                        series: [{
                            name: '中国',
                            type: 'map',
                            mapType: '四川',
                            // tooltip: '1123',
                            selectedMode: 'single',
                            itemStyle: {
                                normal: {
                                    areaStyle: {
                                        color: 'rgba(255,255,255,0.8)',
                                    },
                                    borderColor: 'grey',
                                    label: {
                                        show: true
                                    }
                                },
                                emphasis: {
                                    areaStyle: {
                                        color: 'rgba(255,130,71)',

                                    },
                                    label: {
                                        show: true
                                    }
                                }
                            },
                            data: [{
                                name: '南充市',
                                selected: true
                            }]
                        }]
                    });
                    var ecConfig = require('echarts/config');
                    myChart.on(ecConfig.EVENT.MAP_SELECTED, function (param) {
                        var selected = param.selected;
                        var provence = param.target; //获取所选定的省份
                        console.log(param.target)

                        chinaDisapper()
                        document.getElementById("city").style.display = "block";

                    });
                }
        );
    }


    function chinaDisapper() {
        var t = document.getElementById("main");
        t.style.display = "none"
    };

    function goBack() {
        var t = document.getElementById("main");
        t.style.display = "block";
        drawMap();
        document.getElementById("city").style.display = "none";
        $(".travel-list").css("display", "none");
    }
